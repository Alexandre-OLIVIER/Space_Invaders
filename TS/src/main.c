int main(void)
{
  int i = 0;
	uint8_t xv = 0;
	uint8_t inclav;
	uint8_t yv = 0;
	uint8_t x = 0;
	uint8_t y = 0;
	char ship = 'o';
	char missile = '^';
	char alien = '#';

	serial_init(115200);
	vt100_clear_screen();

  /* TODO - Add your application code here */
	vt100_move(37, 2);
	serial_puts("SPACE INVADERS");
	vt100_move(34, 4);
	serial_puts("by Alexandre OLIVIER");
	vt100_move(33, 20);
	serial_puts("Press <SPACE> to start");


  /* Initialize LEDs */
  STM_EVAL_LEDInit(LED3);
  STM_EVAL_LEDInit(LED4);
  STM_EVAL_LEDInit(LED5);
  STM_EVAL_LEDInit(LED6);

  /* Turn on LEDs */
  STM_EVAL_LEDOn(LED3);
  STM_EVAL_LEDOn(LED4);
  STM_EVAL_LEDOn(LED5);
  STM_EVAL_LEDOn(LED6);

  /* Infinite loop */
  while (1)
  {
	i++;
		inclav = serial_get_last_char();

		if (inclav == 's') /*si on appuye sur espace alors lance la partie*/
		{
			vt100_clear_screen();
			vt100_move(37, 2);
			serial_puts("SPACE INVADERS");
			vt100_move(0, 4);
		}

		if (inclav == 'd') /*Si on appuie sur d = déplacement vers la droite*/
		{
			vt100_move(xv, 20);
			serial_putchar(' ');
			xv += 1;
			vt100_move(xv, 20);
			serial_putchar(ship);
		}
		if (inclav == 'q') /*Si on appuie sur q = déplacement vers la gauche*/
		{
			vt100_move(xv, 20);
			serial_putchar(' ');
			xv -= 1;
			vt100_move(xv, 20);
			serial_putchar(ship);
		}
		if (inclav == 'm')
		{
			serial_putchar(' ');
			y += 1;
			vt100_move(x, y);
			serial_putchar(missile);
		}
  }
}


